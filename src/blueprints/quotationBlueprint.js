export function getQuotationTheme(themeId = 'blue') {
  const theme = quotationThemes.find((item) => item.id === themeId);

  if (!theme) {
    throw new Error(`Unknown quotation theme: ${themeId}`);
  }

  return theme;
}

export function toNumber(value) {
  return Number(value || 0);
}

export function formatThaiDate(value) {
  const [year, month, day] = String(value || '').split('-');

  if (!year || !month || !day) {
    return '-';
  }

  return `${day}/${month}/${Number(year) + 543}`;
}

export function filterQuotationRowsByTheme(rawRows, themeId) {
  return rawRows.filter((row) => row.theme_code === themeId);
}

export function findQuotationThemeIndex(themeId) {
  return quotationThemes.findIndex((theme) => theme.id === themeId);
}

export function normalizeQuotationRows(rawRows) {
  if (!Array.isArray(rawRows) || rawRows.length === 0) {
    throw new Error('normalizeQuotationRows(rawRows) needs at least one quotation row.');
  }

  const firstRow = rawRows[0];
  const items = rawRows.map((row) => {
    const quantity = toNumber(row.quantity);
    const unitPrice = toNumber(row.unit_price);

    return {
      code: row.product_code,
      name: row.product_name,
      size: row.product_size,
      description: row.product_description,
      quantity,
      unit: row.unit,
      unitPrice,
      lineTotal: quantity * unitPrice,
      imagePath: row.imagePath,
    };
  });
  const subtotal = items.reduce((sum, item) => sum + item.lineTotal, 0);
  const discount = toNumber(firstRow.discount);
  const shipping = toNumber(firstRow.shipping);
  const vatRate = toNumber(firstRow.vat_rate) / 100;
  const afterDiscount = subtotal - discount;
  const vat = (afterDiscount + shipping) * vatRate;

  return {
    title: 'ใบเสนอราคา',
    themeId: firstRow.theme_code,
    quoteNo: firstRow.quo_no,
    quoteDate: formatThaiDate(firstRow.quo_date),
    validUntil: formatThaiDate(firstRow.quo_valid_until),
    customer: {
      name: firstRow.quo_cus_name,
      contact: firstRow.quo_cus_contact,
      address: firstRow.quo_cus_address,
      tel: firstRow.quo_tel,
      email: firstRow.quo_email,
    },
    remark: firstRow.quo_remark,
    items,
    totals: {
      subtotal,
      discount,
      afterDiscount,
      shipping,
      vat,
      grandTotal: afterDiscount + shipping + vat,
    },
  };
}

export function buildQuotationModelForTheme(rawRows, themeId) {
  return normalizeQuotationRows(filterQuotationRowsByTheme(rawRows, themeId));
}

